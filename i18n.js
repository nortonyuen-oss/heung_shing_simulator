let currentLanguage = detectInitialLanguage();

function detectInitialLanguage() {
  const saved = localStorage.getItem(I18N_STORAGE_KEY);
  if (saved && LANGUAGES[saved]) return saved;

  const language = navigator.language || '';
  if (/^ja\b/i.test(language)) return 'ja';
  if (/^zh-(tw|hk|mo|hant)\b/i.test(language) || /^zh-hant\b/i.test(language)) return 'zhHant';
  return 'en';
}

function t(key, params = {}) {
  const template = I18N[currentLanguage]?.[key] ?? I18N.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name) => params[name] ?? '');
}

function tMonth(month) {
  return t(`month.${month}`);
}

function getDefaultCityName() {
  return t('city.defaultName');
}

function getCurrentLanguage() {
  return currentLanguage;
}

function setLanguage(language) {
  if (!LANGUAGES[language]) return;
  currentLanguage = language;
  localStorage.setItem(I18N_STORAGE_KEY, language);
  applyTranslations();
  document.dispatchEvent(new CustomEvent('languagechange', { detail: { language } }));
}

function applyTranslations(root = document) {
  document.documentElement.lang = currentLanguage === 'zhHant' ? 'zh-Hant' : currentLanguage;

  root.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-html]').forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.setAttribute('title', t(el.dataset.i18nTitle));
  });
  root.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.setAttribute('placeholder', t(el.dataset.i18nPlaceholder));
  });
}

document.addEventListener('DOMContentLoaded', () => applyTranslations());
